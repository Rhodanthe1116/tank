<template>
  <div
    id="startPage"
    v-bind:style="{
      height: this.startPage.height + 'px',
      width: this.startPage.width + 'px',
    }"
  >
    <h1 id="hhh">TANK</h1>
    <div id="nameHolder1">
      <h1>
